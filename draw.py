import turtle

painter = turtle.Turtle()

for _ in range(3):
	painter.forward(100)
	painter.left(90)
	painter.forward(100)
	painter.left(90)
	painter.forward(100)
	painter.left(90)
	painter.forward(100)

	painter.right(90)
	painter.forward(100)
	painter.right(90)
	painter.forward(100)
	painter.right(90)
	painter.forward(100)
	painter.right(90)
	painter.forward(100)

turtle.done()